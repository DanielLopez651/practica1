public void cargarTelefonosTblTelefonos1(List<Telefono> lista){
        DefaultTableModel modelo =  (DefaultTableModel) tblTelefonos1.getModel();
        modelo.setRowCount(0);
        for(Telefono telefono: lista){
            Object[] rowData={telefono.getCodigo(),telefono.getTipo(),telefono.getNumero(),telefono.getOperadora()};
           modelo.addRow(rowData);
        }
           tblTelefonos1.setModel(modelo);
}     


